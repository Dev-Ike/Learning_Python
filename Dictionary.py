#Dictionary

#Syntax

#dict = {key1:value1, key2:value2, key3:value3...}

my_dict = {"name": "Alice", "age": 30, "city": "New York"}

print(my_dict["name"])

#Adding new values pairs

my_dict["job"] = "Computer Science"
print(my_dict)

#Remove a key value pair
del my_dict["age"]
print(my_dict)

#Replace value of a key
my_dict["city"] = "Paris"
print(my_dict)