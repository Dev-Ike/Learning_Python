# for loop

# 5 books
# b1, b2, b3, b4, b5

for i in range(5):
    print(i)

# i is iterated from 0 - 4
# 5 times



# while loop

# condition is true

count = 0
while count < 5:
    print(count)
    count += 1

# 5 iterations
# count = 0
# count = 1
# count = 2
# count = 3
# count = 4
# count = 5 becomes false

